function loadProducts(index) {
  var grid = document.getElementById("products");
  grid.innerHTML = "";
  products[index].forEach((element) => {
    const data = {
      name: element[0],
      price: element[1],
      image: element[2],
      desc: element[3],
    };
    const searchParams = new URLSearchParams(data);
    grid.innerHTML +=
      '<a href="product-details.html?' +
      searchParams.toString() +
      '" class="grid-item">' +
      '<img src="' +
      element[2] +
      '">' +
      '<div class="container">' +
      "<h6><b>" +
      element[0] +
      "</b></h6>" +
      "<p>" +
      element[1] +
      "</p>" +
      "</div></a>";
  });
}
